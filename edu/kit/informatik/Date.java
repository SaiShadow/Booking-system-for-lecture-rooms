package edu.kit.informatik;

/**
 * This class represents the date.
 * 
 * @author uogok
 * @version 1
 *
 */
public class Date {

    private int date;

    /**
     * Gets the date.
     * 
     * @return the current date.
     */
    public int getDate() {
        return this.date;
    }

    /**
     * Sets the date to the value in the parameter.
     * 
     * @param date the date you want to set it to
     */
    public void setDate(int date) {
        this.date = date;
    }

}
